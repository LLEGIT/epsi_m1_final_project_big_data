package com.starrocks.funcs;

public class BeanDataJava {
    // name
    private String name;
    // score
    private int score;

    public BeanDataJava(String name,int score){
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    @Override
    public String toString() {
        return "{ name:" + this.name + ", score:" + this.score + "}";
    }

}
